
package beginnerjava;


public class AssignmentDemo {
    
    public static void main(String[] args) {
        int x = 5 , y = 7;
        
        x+=y;
        System.out.println(" x = "+x);
        x-=y;
        System.out.println(" x = "+x);
        x*=y;
        System.out.println(" x = "+x);
        x/=y;
        System.out.println(" x = "+x);
        x%=y;
        System.out.println(" x = "+x);
    }
    
}
